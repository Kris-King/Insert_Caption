<h3><a id="Uploading Images">Uploading Images</a></h3>


<p> - Only registered users can upload images. Those that wish to upload
    their own images must first create an account.</p>

<p> - Users can upload images which are one of the following: jpg, gif and png.</p>

<p> - Please be wary of any images you upload, other users may find certain images offensive.</p>

<p> - If you have any issues uploading images to the site please 
    <a href="http://localhost/InsertCaption/web/index.php?page=contact">contact us</a>.
    When you do submit an error report please mention what you were doing that led up to this 
    moment and the error message you received.</p>

<p> - If an image you uploaded is deemed offensive in any matter you will be sent
    an email explaining why and given instructions should you wish to contest this.</p>


<h3><a id="Captions">Captions</a></h3>

<p> - Registered users can create captions for any image they see on this site.</p>

<p> - We will pull any caption which is anyway threatening or sexually explicit.</p>

<p> - We will not tolerate those who use captions to bully other users.</p>

<p> - We will report any user who tries to blackmail others through submitted captions.</p>

<p> - There is no limit on how many captions a user can make on a single image.</p>


<h3><a id="Image Ratings">Image Ratings</a></h3>

<p> - Users with an account have the option of Upvoting or Downvoting images they like or dislike.</p>

<p> - Users both registered or otherwise can determine what images they see on the front page.
    Users have the option of filtering images based on either 'Most Recent' or 'Most Popular'.</p>


<h3><a id="Creating an Account">Creating an Account</a></h3>

<p> - To access most functions on this site users must first create an account.</p>

<p> - Users do not need to create an account if they only wish to view user submitted content.</p>

<p> - Users do not need to pay for access or the right to submit content on this site.</p>

<p> - The setup process is simple and only requires a small amount of information from our users.</p>


<h3><a id="Site Conduct">Site Conduct</a></h3>

<p> - All users must respect the wishes of other users by not harassing them in any way.</p>

<p> - Any offensive remarks made toward other users will be pulled from the site.</p>

<p> - If offending remarks continue from said user, then they will be banned from this site 
    and reported to the proper authorities.</p>

<p> - This site will not tolerate death threats or depictions of a sexual nature directed 
    towards other users. This is a bannable offense.</p>

<p> - We will tolerate efforts by anyone to hack this site or spread malicious content. 
    Anyone found to breach site security protocols will be reported to the F.B.I.</p>

<p> - Treat others with respect on this site and you will be able to freely access content 
    available to you.</p>


<h3><a id="Copyright">Copyright</a></h3>

<p> - We accept most images uploaded by users but will pull any which receive a copyright notice.</p>

<p> - This site will not be held accountable for images uploaded by you which are in anyway 
    breach copyright law.</p>

<p> - You will be contacted when an image you upload receives a copyright notice.</p>

<p> - You do have the right to contest any copyright notice levied against you but you must have 
    valid documentation i.e. image source w/ accompanying license to make a solid case for 
    dismissal.</p>

<p> - While users can contest copyright notices they will not be directed through us. 
    We will provide users with the channel avaliable to them, this will allow them to take 
    up their case with the complainant directly.</p>


